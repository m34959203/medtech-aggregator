# Кириллица → латиница (ГОСТ-подобный, для slug). Спецслучаи источников (например
# 103.kz зовёт Усть-Каменогорск «uk», Семей «semei») разрешаются на стороне сбора.
_TRANSLIT = {
    "а": "a", "б": "b", "в": "v", "г": "g", "д": "d", "е": "e", "ё": "e", "ж": "zh",
    "з": "z", "и": "i", "й": "i", "к": "k", "л": "l", "м": "m", "н": "n", "о": "o",
    "п": "p", "р": "r", "с": "s", "т": "t", "у": "u", "ф": "f", "х": "h", "ц": "c",
    "ч": "ch", "ш": "sh", "щ": "sch", "ъ": "", "ы": "y", "ь": "", "э": "e", "ю": "yu",
    "я": "ya", " ": "-", "-": "-",
}


def slugify(name: str) -> str:
    out = "".join(_TRANSLIT.get(ch, ch) for ch in name.lower())
    return "".join(c for c in out if c.isalnum() or c == "-").strip("-")


def all_cities() -> list[dict]:
    """Все 90 городов: {name, region, status, status_label, slug}."""
    return [
        {"name": n, "region": reg, "status": st,
         "status_label": STATUS_LABEL.get(st, ""), "slug": slugify(n)}
        for (n, reg, st) in CITIES
    ]


def names() -> list[str]:
    return [n for (n, _, _) in CITIES]


# Поиск канонического названия по имени ИЛИ slug (для нормализации входов сбора:
# doq отдаёт «almaty», файлы — «Алматы», 103 — «uk»/«semei» и т.п.).
_BY_KEY: dict[str, str] = {}
_SLUG_ALIASES = {  # частые расхождения источников → канонический slug
    "uk": "ust-kamenogorsk", "semei": "semei", "nur-sultan": "astana",
    "nursultan": "astana", "almaty": "almaty",
}
for _n, _, _ in CITIES:
    _BY_KEY[_n.casefold()] = _n
    _BY_KEY[slugify(_n)] = _n
for _alias, _target in _SLUG_ALIASES.items():
    if _target in _BY_KEY:
        _BY_KEY.setdefault(_alias, _BY_KEY[_target])


def canonical_city(raw: str | None) -> str | None:
    """Любое имя/slug города → каноническое русское название из справочника.

    Неизвестный город возвращается как есть (с заглавной) — рынок шире 90 городов
    лишь формально, но мы не теряем данные источника. None/пусто → None.
    """
    if not raw:
        return None
    key = raw.strip().casefold()
    if not key:
        return None
    return _BY_KEY.get(key) or _BY_KEY.get(slugify(raw.strip())) or raw.strip().title()


