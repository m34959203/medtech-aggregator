"""Категория услуги как ENUM из 4 значений ТЗ §2.2 MedPrice.

ТЗ требует enum: «лаборатория / приём врача / диагностика / процедура». В БД
категория хранится свободным текстом (исторически + MedArchive-специальности),
поэтому приводим любую входную категорию/название к одному из 4 канонов на чтении.
Так выдача API соответствует ТЗ, а внутренняя гибкость справочника сохраняется.
"""
from __future__ import annotations

import enum


class Category(str, enum.Enum):
    """§2.2 ТЗ MedPrice: категория услуги — строгий enum из 4 значений.

    Наследование от ``str`` делает члены полноценными строками: сравнение и
    проверка вхождения с обычными строками (`== "лаборатория"`, `in present`)
    работают, а Pydantic/FastAPI сериализуют их в значение-строку.
    """

    LAB = "лаборатория"
    DOCTOR = "приём врача"
    DIAGNOSTIC = "диагностика"
    PROCEDURE = "процедура"


# Обратная совместимость: модуль-уровневые алиасы и кортеж значений.
LAB = Category.LAB
DOCTOR = Category.DOCTOR
DIAGNOSTIC = Category.DIAGNOSTIC
PROCEDURE = Category.PROCEDURE

ENUM = tuple(Category)

# Подстроки (lower) → каноническая категория. Порядок важен: врач и диагностика
# проверяются до лаборатории (например «диагностика» не должна попасть в лабораторию).
_RULES: list[tuple[tuple[str, ...], str]] = [
    (("прием", "приём", "консультац", "врач", "терапевт", "кардиолог", "невролог",
      "гинеколог", "уролог", "эндокринолог", "офтальмолог", "дерматолог", "лор"), DOCTOR),
    (("узи", "мрт", "кт ", "рентген", "ренген", "диагностик", "эндоскоп", "гастроскоп",
      "колоноскоп", "ээг", "экг", "эхо", "флюорограф", "маммограф", "доплер", "допплер",
      "функциональн", "лучев"), DIAGNOSTIC),
    (("процедур", "манипуляц", "инъекц", "укол", "капельниц", "массаж", "перевязк",
      "физиотерап", "операц", "хирург"), PROCEDURE),
    (("анализ", "лаборатор", "кровь", "крови", "моч", "гематолог", "биохим", "гормон",
      "иммунолог", "пцр", "пробир", "исследовани", "панель", "генетик", "цитолог",
      "гистолог", "микробиолог", "соскоб", "мазок"), LAB),
]


def to_enum(*parts: str) -> Category:
    """Каноническая категория ТЗ по любым подсказкам (категория, специальность, имя)."""
    blob = " ".join(p for p in parts if p).lower()
    if not blob.strip():
        return LAB  # большинство источников-лабораторий → дефолт лаборатория
    for keys, cat in _RULES:
        if any(k in blob for k in keys):
            return cat
    # уже канон?
    for cat in ENUM:
        if cat in blob:
            return cat
    return LAB
