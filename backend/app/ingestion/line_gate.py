"""Входной фильтр строк направления/прайса (TASK 1).

Бланк-направление содержит не только услуги: заголовок, ФИО пациента, дату,
инструкции. Их нельзя матчить и тем более создавать как услуги в справочнике.
`classify_line` отсекает такой шум ДО матчинга.
"""
from __future__ import annotations

import re

# Левая часть «ключ: значение», которая означает НЕ услугу (метаданные бланка).
_NOISE_KEYS = re.compile(
    r"^\s*(пациент|ф\.?и\.?о|фио|дата|время|врач|направлени|направил|кабинет|"
    r"адрес|телефон|тел|полис|иин|номер|№|клиник|организац|отделени|карта|"
    r"подпис|печать|диагноз|пол|возраст|email|e-mail)\b",
    re.IGNORECASE,
)

# Инструкции/служебные фразы (не услуга).
_INSTRUCTION = re.compile(
    r"\b(сдат[ьи]|натощак|предостав|результат[ыа]|подготовк|за\s+\d+\s+час|"
    r"строго\s+натощак|не\s+есть|воздержат|утром\s+нато|по\s+назначени)\b",
    re.IGNORECASE,
)

_DATE = re.compile(r"\b\d{1,2}[.\-/]\d{1,2}[.\-/]\d{2,4}\b")

# Одиночное слово-заголовок (без двоеточия, не all-caps): «Направление», «Результаты».
_HEADER_WORD = re.compile(
    r"^(направлени|результат|исследовани|обследовани|заключени|назначени|"
    r"бланк|форма|примечани)\w*$",
    re.IGNORECASE,
)

# Заголовок-директива: «Направление на анализы», «Направление для исследования»,
# «Бланк направления». Это шапка бланка, а НЕ услуга — даже если содержит слово
# «анализ»/«исследование» (которое иначе включает service-hint и срывает отсев).
_DIRECTIVE_HEADER = re.compile(
    r"^\s*(направлени\w*\s+(на|для|к)\b|бланк\s+направлени|кому\s+выдан)",
    re.IGNORECASE,
)

# Маркеры того, что строка ВСЁ-ТАКИ про услугу (защита от ложного отсева).
_SERVICE_HINT = re.compile(
    r"(анализ|кров|моч|узи|мрт|кт|рентген|при[её]м|консультац|гормон|витамин|"
    r"антитела|онкомаркер|глюкоз|холестерин|ттг|оак|оам|пцр|ифа|маз|посев|"
    r"биохими|коагул|липид|ферритин|билирубин|креатинин|hba1c|hbs|тест\b)",
    re.IGNORECASE,
)


def classify_line(raw: str) -> tuple[str, str]:
    """('service'|'noise', reason). Шум не матчится и не создаёт услуг."""
    s = (raw or "").strip()
    if len(s) < 2:
        return "noise", "пусто/слишком коротко"

    has_service = bool(_SERVICE_HINT.search(s))

    # заголовок-директива бланка («Направление на анализы») — шум ДО service-hint,
    # иначе слово «анализ» внутри шапки делает её «услугой» и ловит ложный матч.
    if _DIRECTIVE_HEADER.match(s):
        return "noise", "заголовок направления"

    # одиночное слово-заголовок («Направление», «Результаты») — не услуга
    if _HEADER_WORD.match(s) and not has_service:
        return "noise", "заголовок"

    # дата без услугового маркера
    if _DATE.search(s) and not has_service:
        return "noise", "дата"

    # «ключ: значение» с шумовым ключом (Пациент:, Дата:, Врач: …) — сильный сигнал,
    # имеет приоритет над service-hint (иначе «Пациент: Тестовый» ловится на «тест»).
    if ":" in s:
        left = s.split(":", 1)[0]
        if _NOISE_KEYS.search(left):
            return "noise", f"метаданные бланка ({left.strip()[:30]})"

    # инструкции (сдать натощак, предоставить результаты …)
    if _INSTRUCTION.search(s) and not has_service:
        return "noise", "инструкция"

    # заголовок CAPS без числовых/услуговых маркеров (РУС, длинный)
    letters = re.sub(r"[^A-Za-zА-Яа-яЁё]", "", s)
    if len(letters) >= 8 and letters.isupper() and not has_service:
        return "noise", "заголовок"

    return "service", ""
