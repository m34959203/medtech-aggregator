"""Декомпозиция панелей и составных строк (TASK 4).

Generic-split по «+»/«,» опасен: «ОАК развёрнутый + лейкоформула + СОЭ» — это ОДНА
услуга (ОАК и так включает их), а «Билирубин общий + прямой + непрямой» — ТРИ разных
аналита. Поэтому декомпозиция СЛОВАРНАЯ: разворачиваем только известные панели и
явные перечисления; всё остальное остаётся атомарным и матчится целиком.
"""
from __future__ import annotations

import re

# Панель → её составляющие (эталонные имена). Источник истины для разбиения.
PANELS: dict[str, list[str]] = {
    "липидограмм": ["Холестерин общий", "ЛПНП", "ЛПВП", "Триглицериды"],
    "липидный профиль": ["Холестерин общий", "ЛПНП", "ЛПВП", "Триглицериды"],
    "коагулограмм": ["ПТИ", "МНО", "АЧТВ", "Фибриноген"],
    "гемостазиограмм": ["ПТИ", "МНО", "АЧТВ", "Фибриноген"],
}


def _bilirubin(key: str) -> list[str] | None:
    """«Билирубин общий + прямой + непрямой» → 3 отдельных аналита."""
    if "билирубин" in key and "прям" in key and "непрям" in key:
        return ["Билирубин общий", "Билирубин прямой", "Билирубин непрямой"]
    return None


def decompose(raw: str) -> list[str] | None:
    """Список под-услуг, если строка — известная панель/составное; иначе None."""
    key = (raw or "").lower()

    for marker, parts in PANELS.items():
        if marker in key:
            return list(parts)

    bil = _bilirubin(key)
    if bil:
        return bil

    # Явный список после «Панель/Профиль/Комплекс: a, b, c» — общий случай.
    m = re.match(r"\s*(?:панель|профиль|комплекс|обследование)[^:]*:\s*(.+)", key, re.IGNORECASE)
    if m:
        parts = [p.strip() for p in re.split(r"[;,/]| и ", m.group(1)) if p.strip()]
        if len(parts) >= 2:
            return parts

    return None
