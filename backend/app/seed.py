def seed(reset: bool = True):
    init_db()
    db = SessionLocal()
    try:
        if reset:
            db.query(Price).delete()
            db.query(ServiceCatalog).delete()
            db.query(Clinic).delete()
            db.commit()

        for name, category, syns in CATALOG:
            db.add(ServiceCatalog(canonical_name=name, category=category, synonyms=syns))
        db.commit()

        clinics = []
        for c in CLINICS:
            clinic = Clinic(**c)
            db.add(clinic)
            clinics.append(clinic)
        db.commit()
        for cl in clinics:
            db.refresh(cl)

        for clinic_idx, source_type, rows in PRICELISTS:
            channel = "push" if source_type == "upload" else "pull"
            fmt = {"upload": "xlsx", "web_scrape": "html", "api": "json"}[source_type]
            items = [RawItem(raw_name=n, price=float(p)) for n, p in rows]
            res = ingest_items(
                db, clinic_id=clinics[clinic_idx].id, channel=channel,
                source_type=source_type, items=items, fmt=fmt, valid_from=date.today(),
            )
            print(f"  {clinics[clinic_idx].name}: {res.items_found} поз., совпало {res.matched}, на проверку {res.needs_review}")

        n_cat = db.query(ServiceCatalog).count()
        n_price = db.query(Price).count()
        print(f"\nИтого: {len(clinics)} клиник, {n_cat} услуг в справочнике, {n_price} цен.")
    finally:
        db.close()


if __name__ == "__main__":
    seed()
